let idade = 100;
if (idade <= 12) {
    console.log('É uma criança');
} else if (idade <= 17) {
    console.log('É um adolescente');
} else if (idade <= 29) {
    console.log('É um jovem');
} else if (idade <= 55) {
    console.log('É um adulto');
} else if (idade <= 99) {
    console.log('É um idoso');
} else {
    console.log('É um dinossauro');
}